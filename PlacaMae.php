<section>
    <h2> DEFINIÇÃO DE PLACA MÃE </h2>
        <h3>
            É uma peça central responsável por conectar e interligar todos os componentes (processador com memória RAM,
             disco rígido, placa gráfica). A placa une todas as partes do sistema numa só rede de fios, porque dispõe de
              caminhos que permitem a troca de informação entre processadores, memórias, placas e etc
            </h3>
</section>
<section id="p1">
    <h2> PASSO 1</h2>
    <h3> 
        A primeira ação que devemos tomar antes de iniciar o procedimento de instalação é ter as mãos limpas e bem secas.
         Uma coisa com a qual devemos nos preocupar é a estática que nossas mãos podem descarregar e, por isso,
          uma pulseira antiestática é essencial para o trabalho, evitando danos na placa
        </h3>
</section>
<section id="p2">
    <h2> PASSO 2</h2>
    <h3>
        Para começar precisaremos de uma chave do tipo Philips, pois todos os parafusos que usaremos são padronizados para esse tipo de chave.
        Vamos separar os parafusos, pois temos dois tipos: parafusos de fixação e os parafusos normais. Os parafusos de fixação serão usados como uma extensão,
        ficando entre a placa metálica do gabinete e a placa mãe. O parafuso normal será usado para fixarmos a placa mãe no gabinete.
    </h3>
    <h3>
        Após os procedimentos acima descritos, partiremos para o trabalho pesado.
        Primeiro vamos verificar o gabinete. Alguns gabinetes nos dão a opção de remover a placa metálica em que a placa mãe é instalada, 
        outros não. No caso específico com o qual estamos trabalhando, nosso gabinete não dá a opção de remover essa placa metálica, 
        então teremos que trabalhar em um espaço físico um pouco mais limitado. 
        Vamos pegar o gabinete e deitá-lo com cuidado em uma superfície plana.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/gab.jpg" width="700" height="300"> 
</div>
   

</section>
<section id="p3">
    <h2> PASSO 3</h2>
    <h3>
        Em seguida, iremos identificar os furos dos parafusos da placa mãe. A identificação é rápida e descomplicada, 
        pois são aqueles que têm o revestimento em metal. Como temos vários tamanhos de placas mães no mercado, 
        existem vários furos no gabinete, então faremos um teste básico. Colocaremos a placa mãe em cima da placa de metal, 
        porém, sem encostar diretamente no gabinete, e já poderemos ver quais são os buracos exatos para nossa placa.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/parafu.jpg" width="700" height="500">
   </div>
    
</section>
<section id="p4">
    <h2> PASSO 4</h2>
    <h3>
        Feito isso, colocaremos os parafusos de fixação na placa de metal, 
        lembrando que temos que colocá-los no local exato que vimos na marcação da placa mãe. 
        Logo após, com muito cuidado, colocaremos a placa mãe exatamente em cima dos parafusos de fixação, 
        lembrando que estes parafusos devem combinar com os furos metalizados da placa. 
        Esses furos são metalizados para que você possa colocar um parafuso diretamente na placa sem que haja algum tipo de dano na mesma, 
        como um curto-circuito.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
       <img src="./src/img/bura.jpg" width="700" height="500">

   </div>
</section>
<section id="p5">
    <h2> PASSO 5</h2>
    <h3>
        Iremos agora para a parte da fixação da placa mãe na placa metálica do gabinete. 
        Após posicionar a placa mãe sobre os parafusos de fixação, parafusaremos cada um dos furos com 
        bastante cuidado e atenção para que a chave não escape e acabe riscando ou danificando a placa.
       </h3> 
