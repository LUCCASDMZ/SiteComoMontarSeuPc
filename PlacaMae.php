<section id="p6">
    <h2> PASSO 6</h2>
    <h3>
        Um gabinete padrão dispõe das seguintes ligações do painel frontal: botão liga/desliga, botão reset e USBs. 
        Esses fios são ligados na placa mãe. Para saber onde conectar esses fios é importante fazer a leitura do manual de instruções, 
        pois a posição pode variar de acordo com o modelo da placa.
    </h3>
