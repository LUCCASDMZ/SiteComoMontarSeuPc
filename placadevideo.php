<section>
    <h2> Considerações Finais</h2>
    <h3>
    As placas de vídeo dedicadas trazem mais desempenho no que diz respeito a gráficos, 
    permitindo executar conteúdos de mídia e jogos com maior resolução. Na hora de comprar, 
    adquira uma GPU com performance adequada para os games que você costuma jogar e fique atento 
    também à exigência energética. Se sua fonte não possuir a capacidade para alimentar todo o sistema, 
    você pode acabar tendo problemas para utilizar o computador.
    </h3>
