<section>
    <h2> DEFINIÇÃO DE PLACA DE VÍDEO </h2>
        <h3>
        A placa de vídeo é o componente responsável por gerar as imagens em um monitor conectado ao computador, 
        além de ajudar o processador com os gráficos mais complexos. 
        Esses dispositivos estão presentes praticamente todos os computadores e podem vir em versões onbord, 
        integradas à placa mãe, ou offboard, que permite ao usuário escolher sua própria configuração. 
        Nesse último caso, o usuário tem a opção de instalar uma GPU em seu computador com o objetivo de 
        apresentar resoluções mais altas ou executar jogos e programas com visuais pesados.
        </h3>
        <h3>
        Veja a seguir como instalar uma placa de vídeo no computador. O procedimento, embora pareça muito técnico, 
        pode ser realizado por qualquer usuário, desde que seja feito com cuidado. Antes de começarmos, 
        é preciso deixar claro que esse processo é exclusivo para desktops (computadores de mesa), 
        já que a grande maioria dos notebooks não permite que esses dispositivos sejam substituídos. Além disso, 
        lembre-se de desconectar seu computador da energia elétrica.
        </h3>
</section>
<section id="p1">
    <h2> PASSO 1</h2>
    <h3> 
    Se você utiliza um computador com vídeo integrado, ou seja, sem uma GPU dedicada, certifique-se que a 
    placa de vídeo que você vai instalar é compatível com o slot de expansão. O padrão atual é o PCI-Express, 
    mas, se seu computador for mais antigo, verifique no manual da placa mãe para descobrir se ela oferece 
    suporte às GPUs mais modernas.
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/pci.jpg" width="700" height="300">
        </h3>
</section>
<section id="p2">
    <h2> PASSO 2</h2>
    <h3>
    Agora que você já sabe qua a placa mãe possui o slot de expansão, verifique se ele está livre, já que é 
    possível que seu computador já venha com uma placa instalada. Uma dica que pode facilitar esse 
    procedimento é observar onde o cabo do monitor se conecta ao gabinete. Se ele estiver em uma 
    posição mais elevada e na vertical, próximo aos conectores USB, RJ45 e mais conexões da placa, 
    isso quer dizer que você está utilizando um vídeo onbord (integrado à placa mãe), sem uma placa de vídeo. 
    Vale ressaltar que as GPUs apresentam os conectores na orientação horizontal e, normalmente, 
    ficam na parte inferior do gabinete, próximo a outras conexões de vídeo ou áudio.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/conec.jpg" width="700" height="300">

   </div>
   
</section>
<section id="p3">
    <h2> PASSO 3</h2>
    <h3>
    Para ter acesso à placa mãe, é preciso abrir o gabinete. Para isso, localize a tampa na posição oposta aos 
    conectores da placa, pois é essa parte que deve ser aberta. Então, verifique quantos parafusos estão 
    prendendo a placa de vídeo ao gabinete. Normalmente, é apenas um, que pode ser acessado pela lateral 
    aberta do componente. No entanto, algumas placas ocupam duas posições na parte traseira do gabinete. 
    Por isso, tenha mesmo certeza de que é apenas um parafuso que mantém o dispositivo preso.
    </h3>
    <h3>
    Assim que a placa for desparafusada, ela ainda não pode ser removida, apesar de apresentar uma pequena mobilidade. 
    Na parte oposta ao lado em que estão os conectores VGA, da placa de vídeo, localize junto à placa mãe uma 
    pequena trava que precisa ser pressionada para, de fato soltar, a GPU. Antes de retirar a placa, 
    observe se existe algum cabo de alimentação conectado a ela e remova-o pressionando uma pequena trava na 
    lateral. Só então, você pode delicadamente puxar a placa de vídeo. De preferência, segure pelas 
    extremidades ou ventoinhas.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/placaenergy.jpg" width="700" height="500">
   </div>
    
</section>
<section id="p4">
    <h2> PASSO 4</h2>
    <h3>
    Agora, seu slot PCI Express está pronto para receber a nova placa de vídeo. Observe que o conector possui uma 
    posição única para seu encaixe. Para não se confundir durante o procedimento, tenha em mente que as ventoinhas 
    da GPU ficam posicionadas para baixo. Assim que você inserir a placa de vídeo no slot, a trava que a 
    prende à placa mãe vai se fechar automaticamente. Verifique se a placa posicionada requer os conectores 
    adicionais de energia e os conecte de acordo com a exigência do dispositivo.
    </h3>
    <h3>
    Em seguida, parafuse a placa de vídeo ao gabinete e faça os primeiros testes da nova GPU. Os modelos mais 
    modernos possuem conexões HDMI, Display Port ou DVI. Essa característica é muito importante, pois vai definir 
    o tipo de cabo que vai ligar sua placa de vídeo ao monitor ou TV. Depois da instalação, 
    certifique-se de que os cabos de saída de imagem foram conectados corretamente.  
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
    <img src="./src/img/placconec.jpg" width="700" height="500">
   </div>
   
</section>
<section id="p5">
    <h2> PASSO 5</h2>
    <h3>
    Se todo o procedimento for realizado corretamente, ao ligar seu computador, a imagem será gerada normalmente 
    no monitor. Então, tudo o que você vai precisar fazer é instalar os drivers mais recentes de sua nova 
    placa de vídeo. Para isso, visite o site da fabricante e tenha em mãos o modelo da GPU e a versão do 
    seu sistema operacional. Durante a instalação, a tela de seu computador pode piscar e apresentar algumas 
    irregularidades, o que é normal e será completamente corrigido quando o PC for reiniciado, ao final da instalação.
       </h3> 
