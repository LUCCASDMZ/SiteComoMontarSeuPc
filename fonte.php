<section>
    <h2> DEFINIÇÃO DE FONTE DE ALIMENTAÇÃO </h2>
        <h3>
        A fonte de alimentação é um componente de aparelhos eletrônicos que serve para transformar a energia 
        elétrica que chega pelas tomadas em uma corrente elétrica contínua. Ou seja, ela recebe a energia 
        em 110V ou 220V e transforma na voltagem adequada para o funcionamento do aparelho, que geralmente é 12V.
            </h3>
</section>
