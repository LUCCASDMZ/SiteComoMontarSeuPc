<section id="p4">
    <h2> PASSO 4</h2>
    <h3>
    Conecte todos os cabos da fonte nos lugares onde os cabos antigos estavam. É importante lembrar que todos os 
    conectores possuem um jeito certo de encaixe. Se você não estiver conseguindo conectá-los, a possibilidade 
    de estar tentando do jeito errado é grande. Gire o conector em 180º e tente novamente. Nunca force-o.
    Alguns cabos ficarão sobrando, e isso é perfeitamente normal. Apenas organize-os de um jeito que 
    não prejudique a ventilação dos componentes internos do computador.
