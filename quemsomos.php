    <h3>Sistema Escolar - Projeto em Experiência do Usuário</h3>
    <p>A finalidade dessa página seria fornecer um guia completo e detalhado para ajudar os usuários interessados em montar seu próprio computador. A página seria útil para entusiastas de tecnologia, pessoas interessadas em aprender sobre hardware de computadores e aqueles que desejam construir um computador personalizado de acordo com suas necessidades, preferências e com as melhores lojas.</p>
    <p>O escopo da página seria abranger todas as etapas envolvidas no processo de montagem de um computador, o conteúdo da página seria voltado para iniciantes e abordaria os seguintes tópicos:</p>
    <ol>
      <li>Peças do PC</li>
      <li>Videos Explicativo</li>
      <li>Fabricantes</li>
      <li>Guia Passo a Passo</li>
      <li>Sites Indicados</li>
      <li>Faça seu orçamento</li>
      <li>Quem Somos</li>
    </ol>
    <p>Os requisitos funcionais (RF) do projeto incluem:</p>
    <ul>
     
    <li>[RF.001] Peças do PC: Escolha cuidadosamente os principais componentes do seu computador, como processador, placa-mãe, memória RAM, armazenamento, placa de vídeo, fonte de alimentação e gabinete, com base na compatibilidade, desempenho, capacidade de upgrade e orçamento.</li>
    <br>
    <br>
    <br>
    <li>[RF.002] Vídeos Explicativo: Assista a vídeos detalhados que mostram todas as peças essenciais de um computador, incluindo processador, placa-mãe, memória RAM, armazenamento, placa de vídeo, fonte de alimentação e gabinete, fornecendo uma visão abrangente do hardware necessário para montar um PC.</li>
    <br>
    <br>
    <li>[RF.003] Fabricantes: Saiba mais sobre os principais fabricantes de componentes de PC, como Apple, Dell, HP, Lenovo, Asus, Acer, entre outros. Descubra informações sobre a reputação desses fabricantes, a confiabilidade de seus produtos, o suporte ao cliente que oferecem, suas linhas de produtos e outras considerações relevantes ao escolher peças para montar seu próprio computador.</li>
    <br>
    <br>
    <li>[RF.004] Guia Passo a Passo: Siga um guia detalhado que irá te orientar em todas as etapas do processo de montagem do seu PC, desde a preparação do local de trabalho até o fechamento do gabinete. Aprenda como instalar corretamente o processador, memória RAM, placa-mãe, armazenamento, placa de vídeo, fonte de alimentação e como gerenciar os cabos internos. Um guia passo a passo te ajudará a montar o seu PC com confiança e obter um resultado bem-sucedido.</li>
    <br>
    <br>
    <li>[RF.005] Sites indicados: Descubra as melhores opções de lojas para comprar peças de PC, tanto online como em lojas físicas. Conheça lojas confiáveis, com boa reputação, ampla variedade de produtos, preços competitivos e opções de entrega ou retirada. Encontre dicas de como escolher a loja certa para adquirir as peças para o seu projeto de montagem de computador de forma conveniente e segura.</li>
    <br>
    <br>  
    <li>[RF.006] Faça seu Orçamento: Depois de aprender a como montar seu computador, é a hora de fazer seu orçamento de acordo com o valor que o usuário esteja disposto a gastar. Indicamos as melhores lojas para comprar.</li>
    <br>
    <br>  
    <li>[RF.007] Quem Somos: Mostramos quem são os criadores do incrível site.</li>
    </ul>
