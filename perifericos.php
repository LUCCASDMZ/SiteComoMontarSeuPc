<section id="p6">
    <h2>FERRAMENTAS</h2>
    <h3>
    Chaves de fenda e chaves “Philips” -
Estas são as ferramentas mais usuais para manutenção, uma vez que computadores e outros equipamentos possuem muitos parafusos, elas são fundamentais para qualquer reparo, pois é com elas que os dispositivos serão abertos.
    </h3>
    <h3>
    Alicates -
Assim como as chaves de fenda, alicates de diferentes tamanhos e pontas são aliados comuns em consertos. Em algumas situações, as chaves mal utilizadas podem danificar ou mesmo estragar parafusos. Essa é uma indicação bastante usual dos alicates.
    </h3><h3>
    Detector de voltagem -
Choques elétricos são um perigo realmente sério e é sempre bom ter uma ferramenta para certificar-se de que a energia foi interrompida antes de realizar um reparo ou manutenção.
    </h3><h3>
    Sugru - 
Essa é uma ferramenta bastante moderna e que pode ser usada em muitos casos, sendo os reparos de cabos e componentes ressecados os mais usuais. O sugru é uma massa de modelar que conserta praticamente qualquer coisa.
    </h3><h3>
    Multímetro -
É um equipamento utilizado para a medição de grandezas elétricas, incorporando diversos instrumentos de medição, como o voltímetro, o amperímetro, o ohmímetro, e pode chegar a conter, em alguns modelos, também um capacímetro, frequencímetro, termômetro, entre outros.
    </h3><h3>
    Câmera térmica -
Uma câmera térmica é um equipamento que captura a luz infravermelha e a converte para faixa visível ao olho humano, permitindo que se possa ver o calor gerado por objetos.
    </h3>
    <h3>
    Pulseira antiestática -
Essa ferramenta é usada para que a carga elétrica normalmente acumulada em nosso organismo seja  descarregada.
    </h3>
    <h3>
    Fita elétrica líquida -
Uma substituta da fita isolante é a fita elétrica líquida. O produto é mais fácil de aplicar que a fita isolante tradicional e garante mais segurança e melhor acabamento estético às instalações elétricas.
    </h3><h3>
    Pinças -
Muitas vezes, o processo de consertar e reparar equipamentos como computadores pode ser comparado a um procedimento cirúrgico. Por isso, o uso de pinças e coletores são ferramentas bastantes usadas, facilitando o trabalho no quesito de alcance ou colocação de peças em locais que exigem precisão.
    </h3>
