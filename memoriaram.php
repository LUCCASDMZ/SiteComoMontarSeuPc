<section>
    <h2> DEFINIÇÃO DE MEMORIA RAM </h2>
        <h3>
            
A memória RAM (Random Access Memory), também conhecida como memória de acesso aleatório, é um tipo de memória de computador 
volátil que armazena temporariamente os dados e as instruções necessárias para o processamento de um sistema em tempo real.
            </h3>
</section>
<section id="p1">
    <h2> PASSO 1</h2>
    <h3> 
        Localize os slots de memória na placa-mãe: Os slots de memória são longos e estreitos, geralmente encontrados próximos ao processador na placa-mãe. 
        Identifique os slots disponíveis para instalar a memória RAM.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/memoriaram1.png" width="700" height="500">
   </div>
    
</section>
<section id="p2">
    <h2> PASSO 2</h2>
    <h3>
        Verifique o tipo de memória suportado: Consulte o manual do usuário da placa-mãe ou o site do fabricante para verificar o tipo de memória RAM suportado. 
        É importante saber o tipo de memória compatível, como DDR4 ou DDR3, e a velocidade suportada.
    </h3>
    
   

</section>
<section id="p3">
    <h2> PASSO 3</h2>
    <h3>
        Remova os suportes de retenção, se necessário: Alguns slots de memória possuem suportes de retenção laterais 
        que precisam ser abertos antes de inserir os módulos de memória. 
        Verifique se é necessário remover esses suportes antes de continuar.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/memoriaram2.png" width="700" height="500">
   </div>
    
</section>
<section id="p4">
    <h2> PASSO 4</h2>
    <h3>
        Alinhe a memória RAM com os slots: Segurando a memória pelos cantos, alinhe os entalhes na parte inferior do módulo de memória com as saliências nos slots de memória. 
        Certifique-se de que a memória esteja na posição correta.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/memoriaram3.png" width="700" height="500">
   </div>
    
</section>
<section id="p5">
    <h2> PASSO 5</h2>
    <h3>
        Insira a memória RAM nos slots, pressionando firmemente: Com cuidado, insira o módulo de memória RAM no slot, 
        aplicando pressão firme e uniforme nos dois lados do módulo até que encaixe completamente no slot. Os suportes de retenção devem se encaixar automaticamente.
       </h3> 
     
    
</section>  
<section id="p6">
    <h2> PASSO 6</h2>
    <h3>
        Após inserir a memória, verifique se ela está corretamente fixada nos slots. 
        Os suportes de retenção devem estar segurando a memória firmemente no lugar.
    </h3>
  
</section>  
<section>
    <h2> Considerações Finais</h2>
    <h3>
        Certifique-se de seguir as instruções específicas da placa-mãe e ter cuidado ao manusear a memória RAM para evitar danos estáticos. 
        Consultar o manual do usuário da placa-mãe é sempre recomendado para obter instruções detalhadas e precisas para a instalação da memória RAM.
    </h3>
</section>  
