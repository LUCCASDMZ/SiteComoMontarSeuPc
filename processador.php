<section>
    <h2> DEFINIÇÃO DE PROCESSADOR </h2>
        <h3>
            Um processador, também conhecido como unidade central de processamento (CPU, do inglês Central Processing Unit), 
            é o principal componente de um computador ou dispositivo eletrônico que executa instruções e realiza cálculos para processar dados. 
            Ele é considerado o "cérebro" do sistema, responsável por controlar e coordenar todas as operações do computador.
            </h3>
</section>
<section id="p1">
    <h2> PASSO 1</h2>
    <h3> 
        Verifique a compatibilidade entre o processador e a placa-mãe: Antes de começar, verifique se o processador é compatível com o soquete da placa-mãe. 
        Consulte as especificações da placa-mãe e do processador para garantir a compatibilidade adequada.
        </h3>
</section>
<section id="p2">
    <h2> PASSO 2</h2>
    <h3>
        Abra a tampa do soquete do processador na placa-mãe: Localize o soquete do processador na placa-mãe. Geralmente, ele é coberto por uma tampa protetora. 
        Abra a tampa com cuidado, seguindo as instruções do fabricante.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/processador1.png" width="700" height="300">

   </div>
   
</section>
<section id="p3">
    <h2> PASSO 3</h2>
    <h3>
        Alinhe e insira suavemente o processador no soquete: Observe a orientação correta do processador em relação ao soquete. 
        Alinhe os pinos do processador com os orifícios do soquete e insira-o de maneira suave e firme. Não force o encaixe.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/processador2.png" width="700" height="500">
   </div>
    
</section>
<section id="p4">
    <h2> PASSO 4</h2>
    <h3>
        Trave o processador no lugar: Depois de inserir o processador corretamente no soquete, feche a tampa do soquete. 
        Dependendo da placa-mãe, você pode precisar pressionar a tampa para baixo ou deslizá-la para trancar o processador no lugar. 
        Certifique-se de que esteja bem fixado.
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
    <img src="./src/img/processador3.png" width="700" height="500">
   </div>
   
</section>
<section id="p5">
    <h2> PASSO 5</h2>
    <h3>
        Instale o cooler sobre o processador: Coloque o cooler de resfriamento sobre o processador. 
        Certifique-se de que esteja alinhado com os orifícios de fixação na placa-mãe. 
        Use os grampos, parafusos ou mecanismos de fixação fornecidos pelo fabricante para prender o cooler de forma segura no lugar.
       </h3> 
       <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/processador4.jpg" width="700" height="500">
   </div>
       
    
</section>  
<section id="p6">
    <h2> PASSO 6</h2>
    <h3>
        Conecte o cabo de alimentação do cooler à placa-mãe: Localize o conector de alimentação do cooler na placa-mãe. 
        Conecte o cabo de alimentação do cooler a esse conector de acordo com as orientações do fabricante. 
        Isso garantirá que o cooler receba energia adequada para o resfriamento do processador.
    </h3>
