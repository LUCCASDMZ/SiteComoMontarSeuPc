<h1>Peças de Computador</h1>
    <p>Um computador é composto por diversas peças, cada uma desempenhando um papel fundamental no funcionamento do dispositivo.</p>
    
    <h2>Processador</h2>
    <p>O processador é o "cérebro" do computador, responsável por executar as instruções e operações necessárias para o funcionamento do sistema.</p>

    <h2>Placa de vídeo</h2>
    <p>As placas de vídeo são peças de hardware que transmitem as imagens para o monitor. São chamadas de processadores gráficos, pois processam os dados e os transformam em imagem.<p>
    
    <h2>Memória RAM</h2>
    <p>A memória RAM é responsável por armazenar temporariamente as informações necessárias para a execução dos programas, permitindo que o computador tenha um acesso mais rápido às informações.</p>
    </div>
    <h2>Placa-mãe</h2>
    <p>A placa-mãe é a peça que conecta todas as outras peças do computador, permitindo que elas trabalhem em conjunto para o funcionamento do dispositivo.</p>
    
    <h2>Disco Rígido / HD ou SSD</h2>
    <p>O disco rígido é responsável por armazenar permanentemente as informações do computador, como o sistema operacional, os programas instalados e os arquivos do usuário.</p>
    
    <h2>Fonte de Alimentação</h2>
    <p>A fonte de alimentação é responsável por fornecer energia elétrica para todas as peças do computador, garantindo que elas funcionem corretamente.</p>

    <h2>Cooler</h2>
    <p>É um sistema de arrefecimento usado em diversos tipos de hardwares eletrônicos com o objetivo de evitar a sobrecarga de calor que estes componentes geram.<p>
