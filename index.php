		<body>
			<h1>Guia para montar um PC do zero</h1>
			
			<h2>Passo 1: Escolha as peças</h2>
			<p>Antes de começar a montar seu PC, é importante escolher as peças certas, como processador, placa-mãe, memória RAM, armazenamento, placa de vídeo, fonte de alimentação, etc.</p>
			
			<h2>Passo 2: Prepare as ferramentas</h2>
			<p>Você precisará de algumas ferramentas básicas, como uma chave de fenda, alicates, uma pulseira antiestática e uma superfície de trabalho limpa e estável.</p>
			
			<h2>Passo 3: Monte a placa-mãe</h2>
			<p>Comece montando a placa-mãe no gabinete e certificando-se de que ela esteja segura. Em seguida, instale a CPU, cooler e memória RAM na placa-mãe.</p>
			
			<h2>Passo 4: Conecte os cabos</h2>
			<p>Conecte os cabos da fonte de alimentação à placa-mãe, placa de vídeo, armazenamento e outros componentes necessários.</p>
			
			<h2>Passo 5: Instale a placa de vídeo</h2>
			<p>Se você tiver uma placa de vídeo dedicada, instale-a na placa-mãe.</p>
			
			<h2>Passo 6: Instale o armazenamento</h2>
			<p>Instale o armazenamento (HD ou SSD) no gabinete e conecte-o à placa-mãe.</p>
			
			<h2>Passo 7: Conecte periféricos</h2>
			<p>Conecte o teclado, mouse, monitor e outros periféricos necessários.</p>
			
			<h2>Passo 8: Ligue o PC</h2>
			<p>Finalmente, ligue o PC e verifique se tudo está funcionando corretamente.</p>
