<section>
    <h2> DEFINIÇÃO DE DISCO RÍGIDO </h2>
        <h3>
        Adicionar um HD SATA extra é uma boa solução para ganhar mais espaço para guardar arquivos no computador. 
        A configuração é um caminho favorável para separar documentos, por exemplo, deixando um Disco Rígido para 
        o sistema operacional e programas e outro apenas para arquivos pessoais. 
            </h3>
</section>
<section id="p1">
    <h2> PASSO 1</h2>
    <h3> 
    Desligue o computador e desconecte todos os cabos para facilitar o manuseio do gabinete. 
    Caso não queira, certifique-se de ter desligado pelo menos o cabo de força, e abra a lateral 
    para ter acesso a parte interna do gabinete
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/fonte.jpg" width="700" height="500"> 
   <img src="./src/img/tampa.jpg" width="700" height="500">
</div>
        </h3>
</section>
<section id="p2">
    <h2> PASSO 2</h2>
    <h3>
    Conecte o cabo de dados SATA em um dos plugs SATA da sua placa mãe. 
    Estes cabos são indicados com os nomes “SATA 1, SATA 2, SATA 3 e SATA 4″. 
    Dê preferência a plugs com numeração posterior ao do seu HD principal;
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/sata.jpg" width="700" height="500"> 
</div>
   

</section>
<section id="p3">
    <h2> PASSO 3</h2>
    <h3>
    Posicione o HD SATA em uma das baias do seu gabinete. Dê preferência para um local onde 
    facilite a ventilação. Não o coloque, por exemplo, logo abaixo ou logo acima do HD 
    principal para não haver acúmulo de calor gerado pelas duas unidades;
    </h3>
    <h3>
    Conecte os cabos de energia e dados no HD SATA extra. Caso o seu cabo de energia seja diferente, 
    padrão IDE, pode ser necessário a troca da fonte ou a compra de um adaptador de força IDE para SATA;
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
   <img src="./src/img/sataenergia.jpg" width="700" height="500">
   </div>
    
</section>
<section id="p4">
    <h2> PASSO 4</h2>
    <h3>
    Feche o computador e conecte os cabos na parte traseira do gabinete. Ao ligar o PC novamente, 
    pressione a tecla “DEL” ou a tecla que ativa o menu da BIOS durante a inicialização;
    </h3>
    <div style="display: flex; justify-content: center; align-items: center;">
       <img src="./src/img/bios.jpg" width="700" height="500">
    </div>
    <h3>
    Certifique-se de que o HD que irá iniciar o sistema operacional seja o HD que tenha o sistema operacional instalado.
    </h3>

    
</section>

<section>
    <h2> Considerações Finais</h2>
    <h3>
    Pronto, agora é só aproveitar o espaço extra. O novo HD também pode armazenar outros sistemas operacionais e programas.
    </h3>
